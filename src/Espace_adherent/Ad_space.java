    public void afficheemprunt(){
        String query="SELECT l.titre, l.nom_auteur, p.date_emprunt, p.date_remettre FROM livre l, pret p WHERE p.id_livre=l.id_livre AND p.id_adherent='"+id+"' ";
        d4.setRowCount(0);
         try{
            rst=stm.executeQuery(query);
            while(rst.next()){
                String title=rst.getString("titre");
                String a=rst.getString("nom_auteur");
                String dd=rst.getString("date_emprunt");
                String d=rst.getString("date_remettre");
                Object[] t={title,a,dd,d};
                d4.addRow(t);
            }
        }catch(Exception e){
            System.out.println("Erreur de chargement de pilote:"+e);
        }       
    }
