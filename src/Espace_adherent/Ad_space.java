    public void affichebook(){
        d3.setRowCount(0);
        String query="SELECT titre, nom_auteur, lieu_edition, date_edition, quantite FROM livre";
        try{
            rst=stm.executeQuery(query);
            while(rst.next()){
                String title=rst.getString("titre");
                String a=rst.getString("nom_auteur");
                String l=rst.getString("lieu_edition");
                String d=rst.getString("date_edition");
                int q=rst.getInt("quantite");
                Object[] t={title,a,l,d,q};
                d3.addRow(t);
            }
        }catch(Exception e){
            System.out.println("Erreur de chargement de pilote:"+e);
        }
    }
