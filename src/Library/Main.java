    private void brechercheradActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_brechercheradActionPerformed
        if(trecherche.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Fill up the form of searching","Error",JOptionPane.ERROR_MESSAGE);
        }
        else{
            int id;
            String nom,prenom,username, password,date_n,email,tele;
            try{
                d.setRowCount(0); //pour vider jTable1
                Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3325/library","root","");
                Statement stm=conn.createStatement();
                String e=comborecherche.getSelectedItem().toString();
                String query="SELECT * FROM adherent WHERE "+e+ " LIKE '%"+trecherche.getText()+"%'";
                ResultSet rst=stm.executeQuery(query);
                while(rst.next()){
                    id=rst.getInt("id_adherent");
                    nom=rst.getString("nom");
                    prenom=rst.getString("prenom");
                    username=rst.getString("username");
                    password=rst.getString("password");
                    date_n=rst.getString("date_n");
                    tele=rst.getString("tele");
                    email=rst.getString("email");
                    Object[] t={id,nom,prenom,date_n,tele,email,username,password};
                    d.addRow(t);                    
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(this,"Error "+e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            }
            if(d.getRowCount()==0){
                JOptionPane.showMessageDialog(null,"Aucun adherent trouve");
                actuatlisermembre();
                afficherAdherent();
            }
        }
    }//GEN-LAST:event_brechercheradActionPerformed

