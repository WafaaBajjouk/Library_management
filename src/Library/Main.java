    private void rechercheActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rechercheActionPerformed
          if(recherch.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Fill up the form of searching","Error",JOptionPane.ERROR_MESSAGE);
        }
        else{
            int id;
            String nom_auteur,date_edition,lieu_edition,titre,quantite;
            try{
                d2.setRowCount(0); //pour vider jTable1
                Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3325/library","root","");
                Statement stm=conn.createStatement();
                String e=comborecherche2.getSelectedItem().toString();
                String query="SELECT * FROM livre WHERE "+e+ " LIKE '%"+recherch.getText()+"%'";
                ResultSet rst=stm.executeQuery(query);
                while(rst.next()){
                    id=rst.getInt("id_livre");
                   titre=rst.getString("titre");
                    nom_auteur=rst.getString("nom_auteur");
                    lieu_edition=rst.getString("lieu_edition");
                    date_edition=rst.getString("date_edition");
                    quantite=rst.getString("quantite");
                    Object[] t={id,titre,nom_auteur,lieu_edition,date_edition,quantite};
                    d2.addRow(t);                    
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(this,"Error "+e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            }
            if(d.getRowCount()==0){
                JOptionPane.showMessageDialog(null,"Aucun Book trouve");
                actuatliserBook();
                afficherBook();
            }
        }
    }//GEN-LAST:event_rechercheActionPerformed

